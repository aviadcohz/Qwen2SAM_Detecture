#!/usr/bin/env python3
"""
Offline Feature Alignment: distill SAM 3's native text-encoder output into
the V7 Bridge using a purely-text dataset.

Teacher:  SAM 3's language_backbone (VETextEncoder, frozen).
          Consumes the description (with <|seg|> stripped) and emits the
          CLIP-style global summary token — specifically the 256-d vector
          at the EOS (<|endoftext|>) position of `text_memory_resized`.
          SAM 3's text backbone is CLIP-derived and packs the sentence
          representation into that single token; mean-pooling would
          dilute the sharp semantic/geometric focus we want to distill.

Student:  Qwen [SEG] hidden state (4096D, from frozen Qwen3-VL's
          language model) -> trainable V7 Bridge (4096 -> 1024) ->
          SAM 3's frozen resizer (1024 -> 256).

Loss:     1 - cos(student, teacher), averaged across the batch
          (user asked for CosineEmbeddingLoss-style objective).
          MSE also available via --loss mse.

Validation (the real signal):
  Every `--val-every` optimiser steps, we run the RWTD Bridge Oracle
  evaluator on the CURRENT Bridge weights and report true mIoU. The
  best Bridge state (highest Oracle mIoU) is saved to
  `aligned_bridge_best.pt`.

RWTD is used ONLY for validation. It is never in the training stream.
The entire training dataset comes from `offline_alignment_dataset.json`
(generated by scripts/generate_alignment_data.py).

Usage:
    python scripts/offline_text_alignment.py \\
        --dataset offline_alignment_dataset.json \\
        --rwtd-gt RWTD_GT.json \\
        --output-dir checkpoints/offline_align \\
        --batch-size 128 --epochs 3 --lr 1e-4 --val-every 500
"""

